// ★ 월요일 research 에이전트가 호출하는 핵심 엔드포인트 — 멱등 UPSERT
// body: { items:[...], source?, note? }
// import 핵심 로직(멱등 UPSERT) — HTTP 라우트와 incoming 파일 인제스트가 공유한다.
function runImport(items, source, note) {
    const now = new Date().toISOString();
    const week = isoWeek(new Date());

    // 조회 인덱스
    const byId = new Map();
    const byKey = new Map();
    for (const m of db.materials) {
      byId.set(m.id, m);
      byKey.set(compositeKey(m.trade, m.name, m.spec), m);
    }
    const idSet = new Set(db.materials.map(m => m.id));

    let imported = 0, updated = 0, unchanged = 0, reverified = 0;
    const skipped = [];
    const newLogs = [];

    for (const raw of items) {
      const it = normalizeImportTaxonomy(raw || {});
      const name = String(it.name || '').trim();
      const trade = String(it.trade || '').trim();
      if (!name || !trade) { skipped.push({ name: name || '(이름없음)', reason: 'trade/name 누락' }); continue; }
      const spec = String(it.spec || '').trim();

      // 매칭: id 우선 → 복합키(trade+name+spec)
      let existing = null;
      if (typeof it.id === 'string' && byId.has(it.id)) existing = byId.get(it.id);
      else existing = byKey.get(compositeKey(trade, name, spec)) || null;

      if (!existing) {
        const rec = buildImportedRecord(it, source, idSet);
        db.materials.push(rec);
        byId.set(rec.id, rec);
        byKey.set(compositeKey(rec.trade, rec.name, rec.spec), rec);
        imported++;
        newLogs.push(makeLog('new', rec, { detail: `신규 등록 (${rec.spec || '-'}) · 시세 ${fmtKRW(rec.price.min)}~${fmtKRW(rec.price.max)}원`, source, week, now }));
        continue;
      }

      // 기존 → 변경 비교
      const before = snapshot(existing);
      const changes = [];
      let priceDir = null;

      const inPrice = normalizePrice(it.price, it.unit);
      if (inPrice && (inPrice.min != null || inPrice.max != null)) {
        if (existing.price.min !== inPrice.min || existing.price.max !== inPrice.max) {
          const oldMid = midPrice(existing.price), newMid = midPrice(inPrice);
          priceDir = (oldMid != null && newMid != null)
            ? (newMid > oldMid ? 'price_up' : (newMid < oldMid ? 'price_down' : 'update'))
            : 'update';
          changes.push(`가격 ${fmtKRW(existing.price.min)}~${fmtKRW(existing.price.max)} → ${fmtKRW(inPrice.min)}~${fmtKRW(inPrice.max)}원`);
          existing.price = {
            min: inPrice.min, max: inPrice.max,
            unit: inPrice.unit || existing.price.unit,
            note: inPrice.note || existing.price.note
          };
        }
      }

      for (const c of applyFieldUpdates(existing, it)) changes.push(c);

      const inDate = (typeof it.collected_date === 'string' && /^\d{4}-\d{2}-\d{2}$/.test(it.collected_date)) ? it.collected_date : null;

      if (changes.length === 0) {
        // 값이 전부 동일해도:
        // ① seed→research 첫 검증은 '검증 승격'으로 기록해 리서치 큐에서 빠지게 한다 (가격 동일 ≠ 미조사)
        // ② 같은 source의 재확인은 collected_date만 앞으로 당긴다 (신선도 회전, changelog 무기록 → 멱등 유지)
        if (existing.source !== source && source !== 'seed') {
          existing.source = source;
          existing.collected_date = inDate || now.slice(0, 10);
          existing.updated_at = now;
          updated++;
          newLogs.push(makeLog('update', existing, { detail: `기준선 시세 검증 — 가격 동일 (${fmtKRW(existing.price.min)}~${fmtKRW(existing.price.max)}원)`, source, week, now, before }));
        } else if (inDate && inDate > (existing.collected_date || '')) {
          existing.collected_date = inDate;
          existing.updated_at = now;
          reverified++;
        } else {
          unchanged++;
        }
        continue;
      }

      existing.updated_at = now;
      existing.source = source;
      existing.collected_date = inDate || now.slice(0, 10);
      updated++;
      const type = priceDir || 'update';
      newLogs.push(makeLog(type, existing, { detail: changes.join(' / '), source, week, now, before }));
    }

    if (newLogs.length) db.changelog.entries.unshift(...newLogs); // 최신순
    db.meta.lastImportAt = now;

    saveMaterials();
    if (newLogs.length) saveChangelog();

    return {
      imported, updated, unchanged, reverified,
      processed: imported + updated + reverified + unchanged,
      total: db.materials.length,           // 갱신 후 전체 자재 수
      changelogAdded: newLogs.length,
      skipped
    };
}

