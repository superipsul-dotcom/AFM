  let base = (typeof it.id === 'string' && it.id.trim())
    ? it.id.trim()
    : (slugify(`${trade}-${category}-${name}-${spec}`) || 'item');
  let id = base, n = 2;
  while (idSet.has(id)) { id = `${base}-${n}`; n++; }
  idSet.add(id);
